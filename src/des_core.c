static uint64_t		apply_ip(uint64_t m)
{
	uint64_t	res;
	int			i;

	res = 0;
	i = 1;
	while (i <= 64)
	{
		res |= MOVEBIT(m, g_ip[i - 1], i);
		i++;
	}
	return (res);
}

static  uint64_t	expand(uint64_t m)
{
	uint64_t	res;
	int			i;

	res = 0;
	i = 1;
	while (i <= 48)
	{
		res |= MOVEBIT(m, g_expansion[i - 1], i);
		i++;
	}
	return (res);
}

static uint64_t		substitude_and_permute(uint64_t m)
{
	uint64_t	res;
	int			i;
	
	res = 0;
	i = 0;
	while (i < 8)
	{
		res |= g_spboxes[i][(m >> 58) & 0x3F];
		m <<= 6;
		i++;
	}
	res <<= 32;
	return (res);
}

static uint64_t	apply_fp(uint64_t m)
{
	uint64_t	res;
	int			i;

	res = 0;
	i = 1;
	while (i <= 64)
	{
		res |= MOVEBIT(m, g_fp[i - 1], i);
		i++;
	}
	return (res);
}

void	des_core(uint64_t keys[16], t_byte input[DES_BLOCK_SIZE],
					t_byte output[DES_BLOCK_SIZE])
{
	uint64_t	m;
	uint64_t	left;
	uint64_t	right;
	uint64_t	temp;
	int			i;

	bytes_to_big_endian_dwords(&m, input, DES_BLOCK_SIZE);
	m = apply_ip(m);
	left = m & 0xFFFFFFFF00000000;
	right = m << 32;
	i = 0;
	while (i < 16)
	{
		temp = substitude_and_permute(expand(right) ^ keys[i]) ^ left;
		left = right;
		right = temp;
		i++;
	}
	m = right | (left >> 32);
	m = apply_fp(m);
	big_endian_dword_to_bytes(m, output);
}
